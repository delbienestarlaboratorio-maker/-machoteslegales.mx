/**
 * Machotes Legales — Datos Jurídicos por Estado de la República Mexicana
 * 
 * Cada estado tiene su propio Código Penal, Código Civil, Código de Procedimientos,
 * Fiscalía, y tribunales. Esta base de datos permite que las plantillas V1
 * se adapten automáticamente al estado del usuario.
 */

export interface StateData {
    code: string            // Abreviatura oficial (AGS, BC, etc.)
    name: string            // Nombre completo
    capital: string         // Capital del estado
    // Códigos y Leyes
    codigoPenal: string     // Nombre oficial del código penal estatal
    codigoCivil: string     // Nombre oficial del código civil estatal
    codigoProc: string      // Código de procedimientos civiles
    codigoProcPenal: string // Código procesal penal (CNPP aplica federal, pero algunos tienen local)
    // Instituciones
    fiscalia: string        // Nombre de la Fiscalía o Procuraduría
    tribunal: string        // Tribunal Superior de Justicia
    juzgados: string        // Denominación de juzgados de primera instancia
    // Artículos clave de fraude (ejemplo de especialización)
    artFraude: string       // Artículo de fraude en código penal estatal
    artRobo: string         // Artículo de robo
    artAmenazas: string     // Artículo de amenazas
    artDanoPropiedad: string // Artículo de daño en propiedad ajena
    artDivorcio: string     // Artículo de divorcio en código civil
    artPension: string      // Artículo de pensión alimenticia
    artArrendamiento: string // Artículo de arrendamiento
}

